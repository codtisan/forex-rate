export type ForexRateData = {
  success: boolean;
  timestamp: number;
  base: string;
  date: string;
  rates: Record<string, number>;
};

const ForexRateDataBorderRed = {
  borderTop: "2px solid red",
  borderBottom: "2px solid red",
  borderLeft: "2px solid red",
  borderRight: "2px solid red",
};

export type ForexRateTableRecord = {
  key: number;
  currency: string;
  rate: number;
  added_rate: number;
  rounded_rate: number;
};

export const ForexRateTablecolumns = [
  {
    title: "Currency",
    dataIndex: "currency",
    key: "currency",
  },
  {
    title: "Rate",
    dataIndex: "rate",
    key: "rate",
    render(value: number, record: ForexRateTableRecord) {
      return {
        props: {
          style: value % 2 === 0 || record.currency === "HKD" ? ForexRateDataBorderRed : {},
        },
        children: <div>{value}</div>,
      };
    },
  },
  {
    title: "AddedRate",
    dataIndex: "added_rate",
    key: "added_rate",
    render(value: number, record: ForexRateTableRecord) {
      return {
        props: {
          style: value % 2 === 0 || record.currency === "HKD" ? ForexRateDataBorderRed : {},
        },
        children: <div>{value}</div>,
      };
    },
  },
  {
    title: "RoundedRate",
    dataIndex: "rounded_rate",
    key: "rounded_rate",
    render(value: number, record: ForexRateTableRecord) {
      const roundedValue = Math.round(value);
      return {
        props: {
          style: roundedValue % 2 === 0 || record.currency === "HKD" ? ForexRateDataBorderRed : {},
        },
        children: <div>{roundedValue}</div>,
      };
    },
  },
];

